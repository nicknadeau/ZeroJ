package net.nicknadeau.zero;

/**
 * A class that holds the major version of this implementation of the Zero specification. This is not to be confused
 * with the version of ZeroJ, which version-controls API changes in the source code. This is the Zero specification
 * version, and therefore pertains to version-controlled consensus requirements for blockchain implementations.
 *
 * The version is held in the static variable: {@link ZeroVersion#ZERO_MAJOR_VERSION}.
 */
public final class ZeroVersion {
    public static final int ZERO_MAJOR_VERSION = 0;
}
